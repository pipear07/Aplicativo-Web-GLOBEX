                                <p>Es un órgano autónomo e independiente, del más alto nivel en la estructura del Estado Colombiano, con personería jurídica, autonomía administrativa, patrimonial y técnica, y no hace parte de ninguna de las ramas del poder público.</p>
                                
                                <p>La CNSC, tal como se conoce actualmente, fue incorporada por la Constitución Nacional en 1991, pues desde antes existía como órgano colegiado encargado de la vigilancia y administración de la carrera administrativa, bajo otros nombres como Consejo Nacional de Administración y Disciplina, Comisión de Reclutamiento, Ascensos y Disciplina y Consejo Superior del Servicio Civil.</p>
                                
                                <p>Actualmente, la CNSC se rige por lo establecido en la Ley 909 de 2004 y las normas concordantes, que regulan el empleo público y la carrera administrativa.</p>
                                
                                <p>Si bien la CNSC, en <b> su rol de administrador de la carrera administrativa</b>, ejerce diferentes funciones, en éste caso, nos vamos a referir a las que más nos interesa respecto a la selección y nombramiento de los candidatos para proveer los empleos de carrera.</p>
                                
                                <p>Son funciones de la CNSC, entre otras:</p>
                                
                                <p><b>*</b> Establecer los lineamientos generales con los que se desarrollarán los procesos de selección para proveer empleos de carrera administrativa.</p>
                               <p><b>*</b> Acreditar las entidades para la realización de los procesos de selección.</p>
                               <p><b>*</b> Elaborar las convocatorias a concurso para el desempeño de empleos públicos de carrera.</p>
                               <p><b>*</b> Realizar los procesos de selección para el ingreso al empleo público, a través de universidades o instituciones educativas contratadas con este fin.</p>
                                <p><b>*</b>Conformar, organizar y manejar el Banco Nacional de Listas de Elegibles</p>
                                <p><b>Siguiendo los lineamientos generales para desarrollar procesos de selección o concursos, la CNSC se rige por los siguientes principios:</b></p>
                                
                               <p><b>*</b> Mérito</p>
                               <p><b>*</b> Libre concurrencia e igualdad para el ingreso</p>
                               <p><b>*</b> Publicidad</p>
                              <p><b>*</b>  Transparencia en la gestión de los procesos</p>
                               <p><b>*</b> Especialización de los órganos técnicos</p>
                              <p><b>*</b>  Garantía de imparcialidad, confiabilidad, eficacia y eficiencia</p>
