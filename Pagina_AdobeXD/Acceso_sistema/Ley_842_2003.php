                                    <span class="section-heading-upper">Etica Profesional del Ingeniero</span>
                                    <span class="section-heading-lower">RESUMEN DE LA LEY 842 DEL 2003</span>
                                </h2>
                                

<p>La <b>Ley 842 de 2003</b>, es el código de ética profesional de la ingeniería, el cual fija los lineamientos éticos por los cuales deben regirse los profesionales de la ingeniería en Colombia.

Entre otros señala cuales son los requisitos para ejercer la ingeniería y define las funciones de los Consejos profesionales de ingeniería. algunos de sus artículos son:</p>

<p><h3>Artículo 12:</h3> experiencia profesional. La tarjeta profesional, solo se suma experiencia o se tiene en cuenta, si se tiene esta tarjeta. Los ingenieros que se graduaron antes de que se  declarara esta ley, si tienen como comprobar su experiencia laboral entonces esta será válida.</p>

<p><h3>Artículo 13:</h3> ejercicio ilegal de la profesión. Una persona que practique o se anuncie como ingeniero o auxiliar de esta profesión y no lo sea en realidad será sancionada por las autoridades o administrativos correspondientes. También quien teniendo la tarjeta profesional suspendida ejerce esta profesión.</p>

<p><h3>Artículo 14:</h3> encubrimiento del ejercicio ilegal de la profesión. El servidor público o la persona que patrocine o encubra el ejercicio ilegal de la ingeniería será sancionado; y si esta persona tiene sus papeles al día como ingeniero podrá ser suspendido por 5 años.</p>

<p><h3>Artículo 15:</h3> el particular, ya sea el ingeniero o la persona que lo encubre, tendrá una multa de 2 a 50 salarios mínimos mensuales legales vigentes. Para decidir de cuanto será la sanción se hacen investigaciones para aplicar la norma; y el dinero le queda al tesoro municipal.</p>

<p><h3>Artículo 16:</h3> copnia da aviso a todas las empresas relacionadas con ingeniería o que utilicen o contraten ingenieros, de la denuncia o suspensión de un ingeniero, esto, para impedir más infracciones de parte del ingeniero suspendido o denunciado y de parte de la empresa o servidor público.</p>
                               
