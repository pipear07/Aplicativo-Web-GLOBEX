                              </center>
                               <p>La <b>Propiedad Intelectual</b> es la denominación que recibe la protección legal sobre toda creación del talento o del ingenio humano, dentro del ámbito científico, literario, artístico, industrial o comercial.</p>

<p>La protección de la propiedad intelectual es de tipo jurídica, sin embargo las leyes que existen no se realiza sobre esta denominación conceptual, sino sobre dos campos muy bien diferenciados: el Derecho de Autor y la Propiedad Industrial.</p>

<p>La protección que la ley colombiana otorga al Derecho de Autor se realiza sobre todas las formas en que se puede expresar las ideas, no requiere ningún registro y perdura durante toda la vida del autor, más 80 años después de su muerte, después de lo cual pasa a ser de dominio público. El registro de la obra ante la Dirección Nacional del Derecho de Autor sólo tiene como finalidad brindar mayor seguridad a los titulares del derecho.</p>

<p>En el caso del <b>Software</b>, la legislación colombiana lo asimila a la escritura de una obra literaria, permitiendo que el código fuente de un programa esté cubierto por la ley de <b>Derechos de Autor</b>.</p>

<p>La <b>Propiedad Industrial</b> por su parte, es la protección que se ejerce sobre las ideas que tienen aplicación en cualquier actividad del sector productivo o de servicios. En Colombia, para oficializar esta protección se requiere un registro formal en la <b>Superintendencia de Industria y Comercio</b> y sólo es válido durante algunos años para asegurar el monopolio de su explotación económica.</p>

<p>La diferencia fundamental entre los Derechos de Autor y la Propiedad Industrial, es que mientras los primeros protegen el medio en el que va la creación y el ingenio artístico, durante toda la vida del autor más un tiempo adicional (80 años), el segundo protege la idea pero sólo en el caso en que tenga una aplicación industrial, y se realiza por un tiempo limitado para asegurar su explotación económica (alrededor de 20 años). En ambos casos, después de pasada la protección, las creaciones pasan a ser de <b>Dominio Publico</b>, lo que significa que cualquier persona o empresa puede utilizarlas sin permiso de nadie y sin tener que pagar por ello, pero siempre reconociendo la autoría.</p>

<p>Si bien la protección de la Propiedad Intelectual se realiza a través de la legislación, y por tanto tiene cobertura en el territorio del país, las leyes y decretos tanto del Derecho de Autor como de la Propiedad Industrial se realizan con base en los acuerdos y tratados de la <b>Organización Mundial de la Propiedad Intelectual (OMPI)</b>, por lo que hace que la mayoría de países en el mundo realizan la protección de forma similar y con mecanismos para la cooperación internacional en caso de pleitos jurídicos por fuera de las fronteras de su territorio.</p>
                               
