                    <div class="row">
                        <div class="col-xl-9 col-lg-10 mx-auto">
                            <div class="bg-faded rounded p-5">
                                <h2 class="section-heading mb-4">
                                    <span class="section-heading-upper">Ley 1341 de 2009 </span>
                                    <span class="section-heading-lower">Ley de TIC</span>
                                </h2>
                               
                               <p>Es la entidad que se encarga de diseñar, adoptar y promover las políticas, planes, programas y proyectos del sector de las Tecnologías de la Información y las Comunicaciones. Dentro de sus funciones está incrementar y facilitar el acceso de todos los habitantes del territorio nacional a las Tecnologías de la Información y las Comunicaciones y a sus beneficios.</p>
                               <br><br>
                                <h2 class="section-heading mb-4">
                                    <span class="section-heading-upper">Se moderniza el Sector de las Tecnologías de la información y las Comunicaciones – TIC, se distribuyen competencias, se crea un regulador único y se dictan otras disposiciones. </span>
                                    <span class="section-heading-lower">LEY 1978 DE 2019 </span>
                                </h2>
                                
                              <p>  <b>Artículo 1°.Objeto.</b> La presente ley determina el marco general para la formulación de las políticas públicas que regirán el sector de las Tecnologías de la Información y las Comunicaciones, su ordenamiento general, el régimen de competencia, la protección al usuario, así como lo concerniente a la cobertura, la calidad del servicio, la promoción de la inversión en el sector y el desarrollo de estas tecnologías, el uso eficiente de las redes y del espectro radioeléctrico, así como las potestades del Estado en relación con la planeación, la gestión, la administración adecuada y eficiente de los recursos, regulación, control y vigilancia del mismo y facilitando el libre acceso y sin discriminación de los habitantes del territorio nacional a la Sociedad de la Información.</p> 

  

<p><b>Parágrafo.</b> El servicio postal continuará rigiéndose por las normas especiales pertinentes, en particular la Ley 1369 de 2009, con las excepciones específicas que contenga la presente Ley.  

El servicio de radiodifusión sonora continuará rigiéndose por las disposiciones específicas expresamente señaladas para ese servicio en la presente Ley.  

Para todos los efectos de la presente Ley, la provisión de redes y servicios de telecomunicaciones incluye la provisión de redes y servicios de televisión. El servicio de televisión abierta radiodifundida continuará rigiéndose por las normas especiales pertinentes, en particular la Ley 182 de 1995, la Ley 335 de 1996, la Ley 680 de 2001 y demás normas que las modifiquen, adicionen o sustituyan.  

Al servicio de radiodifusión sonora y al de televisión abierta radiodifundida les será aplicable la presente Ley en las disposiciones específicas expresamente señaladas para estos servicios.  

Sin perjuicio de la aplicación de los principios generales del derecho.  </p>



<p><b>Artículo 2°.Principios orientadores.</b> La investigación, el fomento, la promoción y el desarrollo de las Tecnologías de la Información y las Comunicaciones son una política de Estado que involucra a todos los sectores y niveles de la administración pública y de la sociedad, para contribuir al desarrollo educativo, cultural, económico, social y político e incrementar la productividad, la competitividad, el respeto a los Derechos Humanos inherentes y la inclusión social. </p>


<p><b>Artículo 3°.</b>Sociedad de la información y del conocimiento. El Estado reconoce que el acceso y uso de las Tecnologías de la Información y las Comunicaciones, el despliegue y uso eficiente de la infraestructura, el desarrollo de contenidos y aplicaciones, la protección a los usuarios, la formación de talento humano en estas tecnologías y su carácter transversal, son pilares para la consolidación de las sociedades de la información y del conocimiento. </p>

  


<p><b>Artículo 4°.</b>Intervención del Estado en el sector de las Tecnologías de la Información y las Comunicaciones. En desarrollo de los principios de intervención contenidos en la Constitución Política, el Estado intervendrá en el sector las Tecnologías de la Información y las Comunicaciones para lograr los fines</p>
  
  
<p>  <b>Parágrafo.</b> El Gobierno Nacional reglamentará lo pertinente al cumplimiento de los anteriores fines, teniendo en cuenta las necesidades de la población y el avance de las tecnologías de la información y las comunicaciones, así como el estado de desarrollo de la Sociedad de la información en el país, para lo cual, se tendrá en cuenta la participación de todos los actores del proceso, en especial a los usuarios. Se exceptúa de la aplicación de los numerales 4 y 9 de este artículo el servicio de radiodifusión sonora. </p>
  
  
 <p> <b>Artículo 5°.</b>Las entidades del orden nacional y territorial y las Tecnologías de la Información y las Comunicaciones, TIC. Las entidades del orden nacional y territorial promoverán, coordinarán y ejecutarán planes, programas y proyectos tendientes a garantizar el acceso y uso de la población, las empresas y las entidades públicas a las Tecnologías de la Información y las Comunicaciones. Para tal efecto, dichas autoridades incentivarán el desarrollo de infraestructura, contenidos y aplicaciones, así como la ubicación estratégica de terminales y equipos que permitan realmente a los ciudadanos acceder a las aplicaciones tecnológicas que beneficien a los ciudadanos, en especial a los vulnerables y de zonas marginadas del país. </p>

  

<p><b>Parágrafo 1°.</b> Las entidades de orden nacional y territorial incrementarán los servicios prestados a los ciudadanos a través del uso de las Tecnologías de la Información y las Comunicaciones. El Gobierno reglamentará las condiciones en que se garantizará el acceso a la información en línea, de manera abierta, ininterrumpida y actualizada, para adelantar trámites frente a entidades públicas, inclusive en el desarrollo de procesos de contratación y el ejercicio del derecho al voto.</p> 

  


<p><b>Artículo 6°.</b> Definición de TIC. Las Tecnologías de la Información y las Comunicaciones (en adelante TIC) son el conjunto de recursos, herramientas, equipos, programas informáticos, aplicaciones, redes y medios que permiten la compilación, procesamiento, almacenamiento, transmisión de información como voz, datos, texto, video e imágenes.  

El Ministerio de Tecnologías de la Información y las Comunicaciones, con el apoyo técnico de la CRC, deberá expedir el glosario de definiciones acordes con los postulados de la UIT y otros organismos internacionales con los cuales sea Colombia firmante de protocolos referidos a estas materias.</p>  
  
  
 <p><b> Artículo 7°.</b> Criterios de interpretación de la Ley. Esta Ley se interpretará en la forma que mejor garantice el desarrollo de los principios orientadores establecidos en la misma, con énfasis en la protección de los usuarios, la garantía y promoción de la libre y leal competencia y la promoción de la inversión.</p>
  
<p><b>  Artículo 8°.</b>Las telecomunicaciones en casos de emergencia, conmoción o calamidad y prevención para dichos eventos. En casos de atención de emergencia, conmoción interna y externa, desastres, o calamidad pública, los proveedores de redes y servicios de telecomunicaciones deberán poner a disposición de las autoridades de manera gratuita y oportuna, las redes y servicios y darán prelación a dichas autoridades en la transmisión de las comunicaciones que aquellas requieran. En cualquier caso se dará prelación absoluta a las transmisiones relacionadas con la protección de la vida humana. Igualmente darán prelación a las autoridades en la transmisión de comunicaciones gratuitas y oportunas para efectos de prevención de desastres, cuando aquellas se consideren indispensables. </p> 

  

<p>Los proveedores de redes y servicios de telecomunicaciones deberán suministrar a las autoridades competentes, sin costo alguno, la información disponible de identificación y de localización del usuario que la entidad solicitante considere útil y relevante para garantizar la atención eficiente en los eventos descritos en el presente artículo. </p>
      
      
      
   <p><b>   Artículo 9°.</b>El sector de las Tecnologías de la Información y las Comunicaciones. El sector de Tecnologías de la Información y las Comunicaciones está compuesto por industrias manufactureras, comerciales y de servicios cuyos productos recogen, procesan, crean, transmiten o muestran datos e información electrónicamente. </p>

  

<p>Para las industrias manufactureras, los productos deben estar diseñados para cumplir la función de tratamiento de la información y la comunicación, incluidas la transmisión y la presentación, y deben utilizar el procesamiento electrónico para detectar, medir y/o registrar fenómenos físicos o para controlar un proceso físico. </p>

  

<p>Para las industrias de servicios, los productos de esta industria deben estar diseñados para permitir la función de tratamiento de la información y la comunicación por medios electrónicos, sin afectar negativamente el medio ambiente. </p>




<p><b>Artículo 10.</b> Habilitación general. A partir de la vigencia de la presente Ley, la provisión de redes y servicios de telecomunicaciones, que es un servicio público bajo la titularidad del Estado, se habilita de manera general, y causará una contraprestación periódica a favor del Fondo Único de Tecnologías de la Información y las Comunicaciones. Esta habilitación comprende, a su vez, la autorización para la instalación, ampliación, modificación, operación y explotación de redes para la prestación de los servicios de telecomunicaciones, se suministren o no al público. La habilitación general a que hace referencia el presente artículo no incluye el derecho al uso del espectro radioeléctrico. </p> 

<p><b>Parágrafo 1°.</b> En materia de habilitación, el servicio de radiodifusión sonora continuará rigiéndose por las disposiciones específicas de la presente Ley. </p> 

<p><b>Parágrafo 2°.</b> En materia de habilitación, el servicio de televisión abierta radiodifundida continuará rigiéndose por las normas especiales pertinentes, en particular la Ley 182 de 1995, la Ley 335 de 1996, la Ley 680 de 2001, y demás normas que las modifiquen, adicionen o sustituyan. No obstante, los operadores del servicio de televisión abierta radiodifundida establecidos a la fecha de entrada en vigencia de la presente Ley podrán acogerse al régimen de habilitación general, de conformidad con el régimen de transición que la Ley disponga.  </p>

<p><b>Parágrafo 3°.</b> En materia del pago de la contraprestación los operadores públicos del servicio de televisión mantendrán las exenciones y excepciones que les sean aplicables a la fecha de entrada en vigencia de la presente Ley.</p>  
