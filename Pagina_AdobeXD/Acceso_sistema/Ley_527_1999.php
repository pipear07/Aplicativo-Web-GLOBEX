                              <p> La Ley 527 de 1999 constituye el marco jurídico integral y general que autoriza el uso de los mensajes de datos en todas las actividades de los sectores público y privado. Su campo de acción va más allá de las operaciones comerciales a través de medios electrónicos (comercio electrónico). Aunque regula aspectos de dicha materia y es conocida como la Ley de Comercio Electrónico, fue redactada de manera que comprenda, salvo dos excepciones, dentro de las cuales no se encuentra el contrato de transporte ni los documentos de transporte, todas las actividades en que se involucre el uso de mensajes de datos.</p>
                               
                               <p>La Ley 527 es, entonces, una ley de carácter probatorio, que permite aplicar las mismas consecuencias jurídicas que ostentaban los medios físicos o tradicionales a la utilización de medios electrónicos. Pero, incluso, lo que hizo la ley fue aclarar esa posibilidad, que en la práctica ya venía dándose en muchos casos, como, por ejemplo, las policitaciones electrónicas usando fax, o contratos electrónicos utilizando teléfono, entre otros.</p>
                               
