                                <h2 class="section-heading mb-4">
                                    <span class="section-heading-upper">Norma ISO 27001</span>
                                    <span class="section-heading-lower">Sistemas de Gestión la Seguridad de la Información</span>
                                </h2>
                               <img src="imagenes/iso.jpg" style="max-width:100%;width:auto;height:auto;">
                              <p> <b>ISO 27001</b> es una norma internacional que permite el aseguramiento, la confidencialidad e integridad de los datos y de la información, así como de los sistemas que la procesan.</p>

<p>El estándar ISO 27001:2013 para los Sistemas Gestión de la Seguridad de la Información permite a las organizaciones la evaluación del riesgo y la aplicación de los controles necesarios para mitigarlos o eliminarlos.</p>

<p>La aplicación de ISO-27001 significa una diferenciación respecto al resto, que mejora la competitividad y la imagen de una organización.</p>

<p>La <b>Gestión de la Seguridad de la Información</b> se complementa con las buenas prácticas o controles establecidos en la norma <b>ISO 27002.</b></p>
               
      <br><br>         
                <h2 class="section-heading mb-4">
                                    <span class="section-heading-upper">Norma ISO 27001</span>
                                    <span class="section-heading-lower">Estructura de la norma ISO 27001</span>
                                </h2>
               
             
              <p><b>1-</b> Objeto y campo de aplicación: La norma comienza aportando unas orientaciones sobre el uso, finalidad y modo de aplicación de este estándar.</p>
               <p><b>2-</b> Referencias Normativas: Recomienda la consulta de ciertos documentos indispensables para la aplicación de ISO27001.</p>
               <p><b>3-</b> Términos y Definiciones: Describe la terminología aplicable a este estándar.</p>
               <p><b>4-</b> Contexto de la Organización: Este es el primer requisito de la norma, el cual recoge indicaciones sobre el conocimiento de la organización y su contexto, la comprensión de las necesidades y expectativas de las partes interesadas y la determinación del alcance del SGSI.</p>
               <p><b>5-</b> Liderazgo: Este apartado destaca la necesidad de que todos los empleados de la organización han de contribuir al establecimiento de la norma. Para ello la alta dirección ha de demostrar su liderazgo y compromiso, ha de elaborar una política de seguridad que conozca toda la organización y ha de asignar roles, responsabilidades y autoridades dentro de la misma.</p>
               <p><b>6-</b> Planificación: Esta es una sección que pone de manifiesto la importancia de la determinación de riesgos y oportunidades a la hora de planificar un Sistema de Gestión de Seguridad de la Información, así como de establecer objetivos de Seguridad de la Información y el modo de lograrlos.</p>
               <p><b>7-</b> Soporte: En esta cláusula la norma señala que para el buen funcionamiento del SGSI la organización debe contar con los recursos, competencias, conciencia, comunicación e información documentada pertinente en cada caso.</p>
               <p><b>8-</b> Operación: Para cumplir con los requisitos de Seguridad de la Información, esta parte de la norma indica que se debe planificar, implementar y controlar los procesos de la organización, hacer una valoración de los riesgos de la Seguridad de la Información y un tratamiento de ellos.</p>
               <p><b>9-</b> Evaluación del Desempeño: En este punto se establece la necesidad y forma de llevar a cabo el seguimiento, la medición, el análisis, la evaluación, la auditoría interna y la revisión por la dirección del Sistema de Gestión de Seguridad de la Información, para asegurar que funciona según lo planificado.</p>
               <p><b>10-</b> Mejora: Por último, en la sección décima vamos a encontrar las obligaciones que tendrá una organización cuando encuentre una no conformidad y la importancia de mejorar continuamente la conveniencia, adecuación y eficacia del SGSI.</p>
                           
                         
                         <br><br>
                           <h2 class="section-heading mb-4">
                                    <span class="section-heading-upper">Norma ISO 27001</span>
                                    <span class="section-heading-lower">Novedades de la ISO 27001: 2013</span>
                                </h2>
                           
                           
                          <p> Esta norma fue publicada recientemente, aportó una serie de cambios con respecto a su antecesora que los usuarios de los SGSI tienen que asimilar para continuar gestionando de forma eficaz la Seguridad de la Información. Las novedades que manifiesta son:</p>

                            <p><b>*</b>No aparece la sección “Enfoque a procesos” con su respectiva metodología basada en el ciclo PHVA, ahora ofrece mayor flexibilidad.</p>    
                            <p><b>*</b>Se elimina la obligatoriedad de algunos documentos, conservando únicamente la declaración de aplicabilidad.</p>
                            <p><b>*</b>Se han revisado los requisitos y controles.</p>
                            <p><b>*</b>Se apuesta por un enfoque del análisis del riesgo en la fase de planificación y operación.</p>
                           
